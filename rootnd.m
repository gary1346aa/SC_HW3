function F = rootnd(x)

R = 4.056734;
S = sum(x);

F(1) = x(1) + x(4) - 3;
F(2) = 2*x(1) + x(2) + x(4) + x(7) + x(8) + x(9) + 2*x(10) - R - 10;
F(3) = 2*x(2) + 2*x(5) + x(6) + x(7) - 8;
F(4) = 2*x(3) + x(5) - 4*R;
F(5) = x(1)*x(5) - 0.193*x(2)*x(4);
F(6) = x(6)*x(2)^1/2 - 0.002597*(x(2)*x(4)*S)^1/2;
F(7) = x(7)*x(4)^1/2 - 0.003448*(x(1)*x(4)*S)^1/2;
F(8) = x(4)*x(8) - 0.00001799*x(2)*S;
F(9) = x(4)*x(9) - 0.0002155*x(1)*(x(3)*S)^1/2;
F(10) = (x(4)^2)*(x(10) - 0.00003846*S);
end